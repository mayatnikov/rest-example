package ru.mvn.domain;

public class Document {
    int dtype;
    int docnum;
    int docser;

    public int getDtype() {
        return dtype;
    }

    public void setDtype(int dtype) {
        this.dtype = dtype;
    }

    public int getDocnum() {
        return docnum;
    }

    public void setDocnum(int docnum) {
        this.docnum = docnum;
    }

    public int getDocser() {
        return docser;
    }

    public void setDocser(int docser) {
        this.docser = docser;
    }
}
